 * 15751 -- палиндром, 3653 -- нет.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun isPalindrome(n: Int): Boolean {
    var number1 = n
    var number2 = 0
    var rem = 0
    while (number1 > 0) {
        rem = number1 % 10
        number1 = number1 / 10
        number2 = number2 * 10
        number2 += rem
    }
    if (n == number2) {
        return true
    }
    return false
}

